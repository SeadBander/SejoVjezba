package two;

public class Kvadrat {
        public int stranica;
        public int povrsina;

    public Kvadrat() {
        this.stranica = stranica;
        this.povrsina = povrsina;
    }

    public void povrsina(int stranica) {
        povrsina = stranica*stranica;
        System.out.println("Povrsina kvadrata je: "+povrsina);
    }

}
